# Comparisons with objects
num1 = 3
num2 = 2

# Equal                     ==
# Not equal                 !=
# Greater than              >
# Less than                 <
# Greater or Equal          >=
# Less or Equal             <=
print(num1 == num2)
print(num1 != num2)
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)

# Type casting (Changing type of an object)
num1 = '100'            # String object
num2 = '200'

num1 = int('100')       # Changing string object to integer
num2 = int('200')

print(num1+num2)
